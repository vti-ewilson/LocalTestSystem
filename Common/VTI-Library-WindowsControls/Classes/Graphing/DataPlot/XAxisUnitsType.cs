namespace VTIWindowsControlLibrary.Classes.Graphing.DataPlot
{
    /// <summary>
    /// Determines the primary units of the X-Axis of the
    /// <see cref="VTIWindowsControlLibrary.Components.Graphing.DataPlotControl">DataPlotControl</see>
    /// </summary>
    public enum XAxisUnitsType
    {
        /// <summary>
        /// Indicates that the units of the X-Axis of the
        /// <see cref="VTIWindowsControlLibrary.Components.Graphing.DataPlotControl">DataPlotControl</see>
        /// will be in seconds
        /// </summary>
        Seconds = 1,

        /// <summary>
        /// Indicates that the units of the X-Axis of the
        /// <see cref="VTIWindowsControlLibrary.Components.Graphing.DataPlotControl">DataPlotControl</see>
        /// will be in minutes
        /// </summary>
        Minutes = 60
    }
}